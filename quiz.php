             A part of Prajwalit this year, QUIZ BYTE is gaining a reputation as one of Prajwalit's biggest events. This year's event expexts to attract larger audiences.<br>
• Quiz Byte is conducted in association with the Quiz Club, one of the most popular and active clubs on campus.<br>
• A quiz covering the vast realms of Science and Technology, it is sure to pique the participant's interest and curiosity, pushing them to blur the boundaries of their imagination.<br>
• Questions are based on a wide variety of topics: technology, science, sci-fi, famous scientists and technocrats, natural phenomena, paradoxes, historical scientific achievements and so on....<br>
• Participants can come in teams of 2 or 3.<br>
• Exciting prizes are up for grabs, for the top 3 teams.<br> A whole host of audience prizes are also up for grabs.<br>
                  <br><br><span style="font-size:200%;">Rules and Regulations</span>
            <br><br>
                 The event comprises of two stages: Prelims and Finals<br>
• Prelims: A written round comprising of 26 questions, including 6 tie-breaker questions. Answer sheets will be
provided to the participating teams. The top 6 teams will make it to the Finals.<br>
• Finals: The top 6 teams will make to the finals. This stage has 3 rounds:<br>
• Round 1: This round consists of 12 questions in the Infinite Pounce format. In order to make things interesting, we'll be<br> asking members of the audience to answer the 12 questions along with the teams on stage. At the end of the round, if the average score of the top 3 audience members is greater than that of the team on-stage with the least score, those 3 audience members will replace the on-stage team with the least score.<br>
• Round 2: This round consists of 4 questions. For each question, a series of 6 progressively easier hints would be given. A team is free to guess the answer at any stage. However, earlier a team chooses to guess the answer, higher the stakes are. A team can change their answer twice within the round. This round will need a bit of strategizing by the teams, in order to minimize the amount of the negatives they incur.<br>
• Audience Round: After Round 2 comes the Audience round. 10-15 questions will be asked to the audience, and prizes will be given to people who answer the questions correctly. Even if you don't make it to the finals, you still can win some pretty cool stuff.<br>
• Final Round: The last round of the quiz is possibly the most interesting. Each team can decide the number of questions, they want to answer. Each team can answer a maximum of 4 questions. The questions are progressively tougher. The marking scheme follows a Geometric sequence. The catch is, once a team gets a question<br>
wrong, their score for the round is reset to 0. So, teams have to be smart about their choice of questions. Also, a team may choose to play a Power Play card for any one question, thereby doubling the points awarded for getting the question right.
• At the end, the top 3 teams will be awarded winners prizes.<br>

